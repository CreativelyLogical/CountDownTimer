package com.example.umutjanmahmut.funwithtimers;

public class CountDown {

    int secondsLeft;

    int countDownTime;

    public CountDown(int setTime) {
        countDownTime = setTime;
    }

    public void tick() {
        for (int i = 0; i < countDownTime; i++) {
            for (int j = 0; j < 1000; j++) {
                continue;
            }

        }
    }



}
